
# Declare an empty list
numbers = []

# Add elements to the list
numbers.append(1)
numbers.append(2)
numbers.append(3)

# Print the list
print(numbers)  # Output: [1, 2, 3]

#| Language   | Data Type            | Add Element Method | Example           |
#| ---------- | -------------------- | ------------------ | ----------------- |
#| C++        | `std::vector<int>`   | `push_back(value)` | `v.push_back(1);` |
#| Java       | `ArrayList<Integer>` | `add(value)`       | `list.add(1);`    |
#| JavaScript | `Array`              | `push(value)`      | `arr.push(1);`    |
